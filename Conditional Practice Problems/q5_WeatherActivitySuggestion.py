# Suggest an activity based on the weather (eg..., Sunny- Go for a walk, rainy- read a book, snowy - Build a snowman)

day = str(input("What's the weather like today?: "))

if (day == "Sunny"):
    print("Go for a walk")
elif(day == "Rainy"):
    print("Read a Book")
elif(day == "Snowy"):
    print("Build a snowman")